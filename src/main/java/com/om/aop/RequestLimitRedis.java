package com.om.aop;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Documented
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface RequestLimitRedis {
    /**
     * Specifies the time limit in seconds (default value: 0s).
     *
     * @return The time limit in seconds.
     */
    long period() default 0;

    /**
     * Specifies the number of allowed requests (default value: 0).
     *
     * @return The number of allowed requests.
     */
    long count() default 0;
}
